    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String patientName = jTextField1.getText();
        String dateStr = jTextField3.getText();

        if (patientName.isEmpty() || dateStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter both name and date.", "Missing Information", JOptionPane.WARNING_MESSAGE);
            return;
        }

        
        Appoint.loadAppointmentsFromFile();

        boolean appointmentFound = false;
        
        

        for (Appoint appt : Appoint.getAllAppointments()) {
            if (appt.getPatientName().equals(patientName) && appt.getDate().equals(dateStr) && appt.getStatus().equals("Scheduled")) {
                appointmentFound = true;
                break;
            }
        }

        if (appointmentFound) {
            int response = JOptionPane.showConfirmDialog(this, "Are you sure you want to cancel the appointment?", "Confirm Cancellation", JOptionPane.YES_NO_OPTION);
            if (response == JOptionPane.YES_OPTION) {
                Appoint.cancelAppointment(patientName, dateStr);
                Appoint.saveAppointmentsToFile();
                bookedTimeslots.remove(dateStr + " " + timeslot);
                JOptionPane.showMessageDialog(this, "Appointment successfully cancelled!", "Success", JOptionPane.INFORMATION_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "No matching appointment found.", "Error", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

