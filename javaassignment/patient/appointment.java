    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
            String patientName = jTextField2.getText();
            String category = jComboBox1.getSelectedItem().toString();
            String date = jTextField4.getText();
            String status = "Scheduled";

            // Check if any field is empty
            if (patientName.isEmpty() || category.isEmpty() || date.isEmpty() || timeslot.isEmpty()) {
                JOptionPane.showMessageDialog(this, "All fields must be filled out.", "Missing Information", JOptionPane.WARNING_MESSAGE);
                return;
            }

            // Load appointments from file
            Appoint.loadAppointmentsFromFile();

            

            String key = date + " " + timeslot;

            if (timeslot != null && !timeslot.isEmpty()) {
                if (bookedTimeslots.contains(key)) {
                    JOptionPane.showMessageDialog(this, "This timeslot is already booked. Please choose another.", "Timeslot Booked", JOptionPane.WARNING_MESSAGE);
                } else {
                    // Proceed to create a new appointment
                    Appoint.createAppointment(patientName, date, timeslot, category, status);
                    Appoint.loadAppointmentsFromFile(); // Reload the appointments after adding a new one
                    bookedTimeslots.add(key);
                    JOptionPane.showMessageDialog(this, "Appointment successfully booked!", "Success", JOptionPane.INFORMATION_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Please select a timeslot.", "No Timeslot Selected", JOptionPane.WARNING_MESSAGE);
            }
    }//GEN-LAST:event_jButton1ActionPerformed

