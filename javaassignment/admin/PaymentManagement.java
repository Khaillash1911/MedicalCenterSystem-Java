    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(row == -1){
            JOptionPane.showMessageDialog(null, "Please select a row to delete.");
        } else {
            int response = JOptionPane.showConfirmDialog(null, "Do you really want to delete this patient's payment?", "Confirm Deletion", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

            if (response == JOptionPane.YES_OPTION) {
                paymentManager.deletePayment(row);
                JOptionPane.showMessageDialog(this, "Payment deleted successfully!");
                row = -1;
                clearTextField();
            }
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

