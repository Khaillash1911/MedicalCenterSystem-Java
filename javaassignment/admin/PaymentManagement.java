    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String patientName = jTextField1.getText();
        String doctorName = jTextField2.getText();
        String date = jTextField3.getText();
        String paymentStatus = (String) jComboBox1.getSelectedItem();
        double amount = Double.parseDouble(jTextField4.getText());
        
        if (patientName.isEmpty() || doctorName.isEmpty() || date.isEmpty() || paymentStatus.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please fill all fields.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        paymentManager.addPayment(patientName, doctorName, date, paymentStatus, amount);
        JOptionPane.showMessageDialog(this, "Payment added successfully!");
        clearTextField();
        

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        if (row == -1) {
            JOptionPane.showMessageDialog(null, "Please select a payment to edit.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        String patientName= String.valueOf(model.getValueAt(row,0));
        String doctorName = String.valueOf(model.getValueAt(row,1));
        String date = String.valueOf(model.getValueAt(row,2));
        String paymentStatus = (String) jComboBox1.getSelectedItem();
        double amount = Double.parseDouble(jTextField4.getText());
        
        if (patientName.isEmpty() || doctorName.isEmpty() || date.isEmpty() || paymentStatus.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please fill all fields.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        paymentManager.editPayment(row, patientName, doctorName, date, paymentStatus, amount);
        JOptionPane.showMessageDialog(this, "Payment edited successfully!");
        clearTextField();
        
    }//GEN-LAST:event_jButton2ActionPerformed

