    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        if (row == -1){
            JOptionPane.showMessageDialog(this, "Please select an appointment from the table.");
            return;
        }
        
        String patientName = String.valueOf(model1.getValueAt(row,0));
        String date = String.valueOf(model1.getValueAt(row,1));
        String time = String.valueOf(model1.getValueAt(row,2));
        String category = String.valueOf(model1.getValueAt(row,3));
        String status = String.valueOf(model1.getValueAt(row,4)); 
        String doctorName = jTextField7.getText();
        
        
        if (doctorName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Doctor name must be provided.");
            return;
        }
        
        String[] appointmentsRow = {patientName, date, time, category, status, doctorName};
        String line = String.join(", ", appointmentsRow);

        // Write the line to the file
        try (FileWriter fw = new FileWriter("src\\textfiles\\appointments.txt", true);
             BufferedWriter bw = new BufferedWriter(fw)) {
            bw.write(line);
            bw.newLine(); // Adds a new line after each entry
            JOptionPane.showMessageDialog(this, "Appointment added successfully.");
        } catch (IOException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error writing to the file.");
        }
        
        model1.removeRow(row);

    // Rewrite appointments.txt to reflect the updated table
        try (FileWriter fw = new FileWriter("src\\textfiles\\appointment.txt");
             BufferedWriter bw = new BufferedWriter(fw)) {
            for (int i = 0; i < model1.getRowCount(); i++) {
                String[] rowValues = new String[model1.getColumnCount()];
                for (int j = 0; j < model1.getColumnCount(); j++) {
                    rowValues[j] = String.valueOf(model1.getValueAt(i, j));
                }
                String rowLine = String.join(", ", rowValues);
                bw.write(rowLine);
                bw.newLine(); // Adds a new line after each entry
            }
            JOptionPane.showMessageDialog(this, "Appointment added and selected row removed successfully.");
        } catch (IOException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error updating the file.");
        }
                
        
        
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

    
    
    
