    private void loadScheduleFromFile() {
        try {
            FileReader fr = new FileReader("src\\textfiles\\schedule.txt");
            BufferedReader br = new BufferedReader(fr);
            String line;
            while ((line = br.readLine()) != null) {
                String[] values = line.split(", ");
                model.addRow(values);
            }
            br.close();
            fr.close();
        } catch (IOException e) {
            e.printStackTrace();
        }
    }
    
