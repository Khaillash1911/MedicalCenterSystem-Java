    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String inputDate = jTextField1.getText();

        if (inputDate.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter a date.");
            return;
        }

        DefaultTableModel filteredModel = new DefaultTableModel();
        filteredModel.setColumnIdentifiers(columnName);

        try (BufferedReader br = new BufferedReader(new FileReader("src\\textfiles\\schedule.txt"))) {
            String line;
            while ((line = br.readLine()) != null) {
                String[] values = line.split(", ");
                if (values.length >= 3 && values[2].equals(inputDate)) {
                    filteredModel.addRow(values);
                }
            }
        } catch (IOException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error reading the file.");
            return;
        }

        if (filteredModel.getRowCount() > 0) {
            jTable1.setModel(filteredModel);
        } else {
            JOptionPane.showMessageDialog(null, "No entries found for the given date.");
        }
       
       
    }//GEN-LAST:event_jButton1ActionPerformed

