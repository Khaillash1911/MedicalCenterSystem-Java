    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       
        if (row == -1) {
            JOptionPane.showMessageDialog(null, "Please select a doctor to edit.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        String doctorName = jTextField1.getText();
        String doctorMail = jTextField2.getText();
        String doctorPass = jTextField3.getText();
        String doctorSpec = jTextArea1.getText();
        
        if (doctorName.isEmpty() || doctorMail.isEmpty() || doctorPass.isEmpty() || doctorSpec.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please fill all fields.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        doctorManager.editDoctor(row, doctorName, doctorMail, doctorPass, doctorSpec);
        JOptionPane.showMessageDialog(this, "Doctor edited successfully!");
        clearTextField();
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jTable1MouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseReleased
        row = jTable1.getSelectedRow();
        
        
        String doctorName = String.valueOf(doctorManager.getModel().getValueAt(row, 0));
        String doctorMail = String.valueOf(doctorManager.getModel().getValueAt(row, 1));
        String doctorPass = String.valueOf(doctorManager.getModel().getValueAt(row, 2));
        String doctorSpec = String.valueOf(doctorManager.getModel().getValueAt(row, 3));
        
        
        jTextField1.setText(doctorName);
        jTextField2.setText(doctorMail);
        jTextField3.setText(doctorPass);
        jTextArea1.setText(doctorSpec);
        
        
    }//GEN-LAST:event_jTable1MouseReleased

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(row == -1){
            JOptionPane.showMessageDialog(null, "Please select a row to delete.");
        } else {
            int response = JOptionPane.showConfirmDialog(null, "Do you really want to delete this doctor?", "Confirm Deletion", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

            if (response == JOptionPane.YES_OPTION) {
                doctorManager.deleteDoctor(row);
                JOptionPane.showMessageDialog(this, "Doctor deleted successfully!");
                row = -1;
                clearTextField();
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

