    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String doctorName = jTextField1.getText();
        String doctorMail = jTextField2.getText();
        String doctorPass = jTextField3.getText();
        String doctorSpec = jTextArea1.getText();
        
        if (doctorName.isEmpty() || doctorMail.isEmpty() || doctorPass.isEmpty() || doctorSpec.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please fill all fields.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        doctorManager.addDoctor(doctorName, doctorMail, doctorPass, doctorSpec);
        JOptionPane.showMessageDialog(this, "Doctor added successfully!");
        clearTextField();
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

